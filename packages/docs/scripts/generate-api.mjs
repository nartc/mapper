import { execFileSync } from "node:child_process";
import { createRequire } from "node:module";
import {
  existsSync,
  readdirSync,
  readFileSync,
  rmSync,
  statSync,
  writeFileSync,
} from "node:fs";
import { dirname, join, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const require = createRequire(import.meta.url);
const docsRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const packagesRoot = resolve(docsRoot, "..");
const apiRoot = join(docsRoot, "docs", "api");
const typedocPackage = require.resolve("typedoc/package.json");
const typedocCli = join(dirname(typedocPackage), "bin", "typedoc");
const optionsFile = join(docsRoot, "typedoc.json");
const tsconfigFile = join(docsRoot, "tsconfig.typedoc.json");

const packages = [
  { slug: "core", name: "@automapper/core" },
  { slug: "classes", name: "@automapper/classes" },
  { slug: "pojos", name: "@automapper/pojos" },
  { slug: "mikro", name: "@automapper/mikro" },
  { slug: "sequelize", name: "@automapper/sequelize" },
  { slug: "nestjs", name: "@automapper/nestjs" },
];

for (const packageInfo of packages) {
  const outputDirectory = join(apiRoot, packageInfo.slug);
  const entryPoint = join(packagesRoot, packageInfo.slug, "src", "index.ts");

  rmSync(outputDirectory, { recursive: true, force: true });

  execFileSync(
    process.execPath,
    [
      typedocCli,
      "--options",
      optionsFile,
      "--tsconfig",
      tsconfigFile,
      "--entryPoints",
      entryPoint,
      "--out",
      outputDirectory,
      "--name",
      packageInfo.name,
    ],
    { cwd: docsRoot, stdio: "inherit" },
  );

  prepareMarkdown(outputDirectory, packageInfo.name);
  writePackageMeta(outputDirectory, packageInfo.name);
}

console.log(`Generated API reference for ${packages.length} packages.`);

function prepareMarkdown(directory, packageName) {
  for (const entry of readdirSync(directory)) {
    const path = join(directory, entry);

    if (statSync(path).isDirectory()) {
      prepareMarkdown(path, packageName);
      continue;
    }

    if (!path.endsWith(".md")) {
      continue;
    }

    const source = readFileSync(path, "utf8");
    const titleMatch = source.match(/^# (.+)$/m);

    if (!titleMatch) {
      throw new Error(
        `TypeDoc output has no page title: ${relative(docsRoot, path)}`,
      );
    }

    const title = titleMatch[1].trim();
    const description =
      title === packageName
        ? `Public API reference for ${packageName}.`
        : `${title} API reference from ${packageName}.`;
    const body = [
      source.slice(0, titleMatch.index),
      source.slice((titleMatch.index ?? 0) + titleMatch[0].length),
    ]
      .join("")
      .replace(/^\s+/, "")
      .replace(
        /\]\((?![a-z][a-z\d+.-]*:|\/|#)([^)\s]+)\.md(#[^)]+)?\)/giu,
        (_, target, hash = "") => `](${target}${hash})`,
      );

    writeFileSync(
      path,
      [
        "---",
        `title: ${JSON.stringify(title)}`,
        `description: ${JSON.stringify(description)}`,
        "---",
        "",
        "<!-- Generated by scripts/generate-api.mjs. Do not edit. -->",
        "",
        body.trimStart(),
      ].join("\n"),
    );
  }
}

function writePackageMeta(directory, packageName) {
  const preferredOrder = [
    "index",
    "classes",
    "interfaces",
    "functions",
    "type-aliases",
    "enumerations",
    "variables",
  ];
  const pages = preferredOrder.filter((page) => {
    if (page === "index") {
      return existsSync(join(directory, "index.md"));
    }

    return existsSync(join(directory, page));
  });

  writeFileSync(
    join(directory, "meta.ts"),
    [
      'import { defineMeta } from "blume";',
      "",
      "export default defineMeta({",
      `  title: ${JSON.stringify(packageName)},`,
      `  pages: ${JSON.stringify(pages)},`,
      "});",
      "",
    ].join("\n"),
  );
}
